
example = "That is Alice's cat."
print(example)

escapeChar = 'That is Alice\'s cat.'
print(escapeChar)


"""
\' single quote
\" double quote
\t tab
\n newline or line break
\\ backslash
"""

print('Hello there!\nHow are you?\nI\'m fine.')

# raw string
print(r'That is Carol\'s cat.')

# triple quotes
print('''Hi Sally,

My name is Tony.''')

